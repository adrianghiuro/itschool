public class Variabile{

	public static void main(String args[]){
		short variabila1 = 123;
		System.out.println(variabila1);
		byte variabila2 = 3;
		System.out.println(variabila2);
		int variabila3 = 312;
		System.out.println(variabila3);
		float variabila4 = 1231.35F;
		System.out.println(variabila4);
		double variabila5 = 123.354D;
		System.out.println(variabila5);
		long variabila6 = 12351512;
		System.out.println(variabila6);
		char variabila7 = 'C';
		System.out.println(variabila7);
		boolean variabila8 = true;
		System.out.println(variabila8);
	}
}