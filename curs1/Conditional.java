public class Conditional{
	public static void main(String args[]){
		int a = 13;
		int b = 123;
		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a >= b);
		System.out.println(a <= b);

		boolean g = true;
		boolean h = false;

		System.out.println(g || h);

		String a="abs";
		System.out.println(a instanceof String);
	}
}
