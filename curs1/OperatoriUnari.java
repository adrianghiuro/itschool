public class OperatoriUnari{
	public static void main(String args[]){
		int a = 1;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(a);
		System.out.println(a--);
		System.out.println(--a);
		System.out.println(a);
		System.out.println(a+=2);
		boolean verif=true;
		System.out.println(verif);
		System.out.println(!verif);




		}
}