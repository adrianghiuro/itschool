class ArrayExemple{
	public static void main(String args[]){
		//un array def + size initialization + element initialization
		int[] sir = new int[40];
		sir[0] = 1;
		sir[1] = 2;
		sir[2] = 3;
		sir[3] = 4;
		System.out.println(sir[3]);
		System.out.println(sir.length);

		//def + initialization

		short[] das = new short[]{1,2,3,4,5};
		System.out.println(das[2] );
		System.out.println(das.length);

		// def + intitialization + change something
	}
}